<?php

/**
 * 
 * 
 * Return the sum of two numbers
 * 
 * @param integer $a the first number
 * 
 * @param integer $b The second number
 * 
 * @return integer The sum of the two numbers
 * 
 */

 function add($a, $b)
 {
     return $a + $b;
 }

 function addsub($a, $b)
 {
     return $a - $b;
 }


 function addmult($a, $b)
 {
     return $a * $b;
 }


 function adddiv($a, $b)
 {
     return $a / $b;
 }

